void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	//see stm321xx_hal_tim.c for different callback function names
	//for timer 3, Timer 3 use update event interrupt
	
	if ((*htim).Instance==TIM2)	{																	//use TIM2 instead of TIM3
		// since only TIM3 use base function, this line is actually not needed
	
		//HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_8);
		//HAL_GPIO_TogglePin(LEDG_GPIO_Port, LEDG_Pin);
		
		if(change_freq_flag && phase_counter == 1){						//if change freq and correct phase to change
			change_freq_flag = 0;																// reset flag
			freq_counter = freq_counter%2 + 1;									// toggle freq_counter 1 or 2
		}
		
		
		TIM2_counter = (TIM2_counter+1)%freq_counter;			//change freq
		
		if(TIM2_counter == 0){
			BSP_LED_Toggle(LED5);			//toggle LED4 with BSP call same config as tutorial
			phase_counter = (phase_counter+1)%4;
		}
	}
}

